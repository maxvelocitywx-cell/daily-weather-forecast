// Filter to get only active CONUS stations
export const ACTIVE_CONUS_STATIONS = RAOB_STATIONS.filter(
  (s) => s.active && !['AK', 'HI'].includes(s.state)
);

// Get station by ID
export function getStationById(id: string): RAOBStation | undefined {
  return RAOB_STATIONS.find((s) => s.id === id || s.wmo_id === id || s.icao === id);
}

// Get stations by state
export function getStationsByState(state: string): RAOBStation[] {
  return RAOB_STATIONS.filter((s) => s.state === state && s.active);
}

// Get nearest station to a point
export function getNearestStation(lat: number, lon: number): RAOBStation | null {
  let nearest: RAOBStation | null = null;
  let minDist = Infinity;

  for (const station of ACTIVE_CONUS_STATIONS) {
    const dist = Math.sqrt(
      Math.pow(station.lat - lat, 2) + Math.pow(station.lon - lon, 2)
    );
    if (dist < minDist) {
      minDist = dist;
      nearest = station;
    }
  }

  return nearest;
}

// Convert station list to GeoJSON for map display
export function stationsToGeoJSON(): GeoJSON.FeatureCollection {
  return {
    type: 'FeatureCollection',
    features: ACTIVE_CONUS_STATIONS.map((station) => ({
      type: 'Feature' as const,
      properties: {
        id: station.id,
        wmo_id: station.wmo_id,
        icao: station.icao,
        name: station.name,
        state: station.state,
        elevation_m: station.elevation_m,
        obs_times: station.obs_times,
      },
      geometry: {
        type: 'Point' as const,
        coordinates: [station.lon, station.lat],
      },
    })),
  };
}
